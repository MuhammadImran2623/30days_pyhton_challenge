# Calculate the area of a rectangle using user-input length and width


length = input("Enter length of the rectangle: ") # the length variable value is in strong form 

width  = input("Enter width of the rectangle:  ") # the width  variable value is in strong form

area = int(length) * int(width) # here we are type casting both the length and width into integer from string  and saved in area variable 

print(f"Area of a Rectangle is {area}")